import re
text="abra kadabra"
if re.search(r"ab*",text):
    print("FOUND!")
else :
    print("NOT FOUND")
"""ab* регулярлы өрнегі келесідей мағына береді:

a – 'a' әрпін білдіреді. Яғни, жолда 'a' болуы керек.
b* – 'b' әрпінен нөл немесе одан көп болуы керек. * операторы 
"нөл немесе көп" 
дегенді білдіреді."""