text = "Hello world"
# 1. Алғашқы немесе соңғы бірнеше символды алу
print(text[:5])     # 'Hello' – алғашқы 5 әріп
print(text[-5:])    # 'world' – соңғы 5 әріп
print(text[:])      # 'Hello world' – толық жол

# 2. Кері айналдыру және аралықпен таңдау
print(text[::-1])   # 'dlrow olleH' – кері жол
print(text[::-2])   # 'drwolH' – кері бағытта әр екінші әріп

# 3. Аралық таңдау
print(text[2:8])    # 'llo wo' – 2-ден бастап 8-ге дейін
print(text[::3])    # 'Hlwl' – әр үшінші әріп

# 4. Теріс индекстермен пайдалану
print(text[-7:-2])  # 'o wor' – соңынан санау
print(text[-1:-6:-1])  # 'dlrow' – соңғы бес әріпті кері алу

# 5. Қадам (step) арқылы әріп таңдау
print(text[1::2])   # 'el ol' – 1-индекстен бастап әр екінші әріп
print(text[::4])    # 'Hor' – әр төртінші әріп

# 6. Бір әріпті бірнеше тәсілмен алу
print(text[0])      # 'H' – алғашқы әріп
print(text[-11])   # 'H' – соңынан санағанда да алғашқы әріп

# 7. Күрделі мысалдар
print(text[6:][::-1])  # 'dlrow' – "world" сөзін кері алу
print(text[:5:2])     # 'Hlo' – "Hello" сөзінің әр екінші әрпін алу
print(text[3:8])      # 'lo wo' – "lo wo" бөлігін алу
